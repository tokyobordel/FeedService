// Package models содержит определения структур данных, используемых
// в приложении (посты, пользователи, связи изображений с постами и т.д.).
package models

// ImagePost представляет связь между постом и изображением в базе данных.
// Используется в таблице image_post для хранения идентификаторов изображений,
// прикреплённых к посту.
type ImagePost struct {
	ID      int `json:"id"`       // уникальный идентификатор связи
	PostID  int `json:"post_id"`  // ID поста, к которому относится изображение
	ImageID int `json:"image_id"` // ID изображения во внешнем сервисе
}
